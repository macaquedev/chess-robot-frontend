def evaluate_board(board):
    total_eval = 0
    for i in range(8):
        for j in range(8):
            if board[i][j][0] != ' ':
                total_eval += get_piece_value(board[i][j][0], board[i][j][2], i, j)
    return total_eval


def get_piece_value(piece, check_colour, i, j):
    i = 7 - i
    colour = check_colour == 'W'

    absolute_value = 0
    if piece == 'P':
        absolute_value = 10 + (pawn_eval_white[i][j] if colour else pawn_eval_black[i][j])
    elif piece == 'N':
        absolute_value = 30 + knight_eval[i][j]
    elif piece == 'B':
        absolute_value = 30 + (bishop_eval_white[i][j] if colour else bishop_eval_black[i][j])
    elif piece == 'R':
        absolute_value = 50 + (rook_eval_white[i][j] if colour else rook_eval_black[i][j])
    elif piece == 'Q':
        absolute_value = 90 + queen_eval[i][j]
    elif piece == 'K':
        absolute_value = 900 + (king_eval_white[i][j] if colour else king_eval_black[i][j])

    if colour:
        return absolute_value
    else:
        return -absolute_value
